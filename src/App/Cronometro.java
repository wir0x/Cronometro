    private final Timer timer;  // Variable timer
    private int h, m, s, ms;    // variables de hora, minuto, segundo, milisegundo

    private final ActionListener acciones = new ActionListener() {

        @Override
        public void actionPerformed(ActionEvent ae) {
            ms++;
            // Milisegundos
            if (ms == 100) {
                ms = 0;
                s++;
                // Segundos
                if (s == 60) {
                    s = 0;
                    m++;
                    // Minutos        
                    if (m == 60) {
                        m = 0;
                        h++;
                    }
                }
            }
            actualizar();
        }
    };

    // Inicio actualizar
    private void actualizar() {

        if (h <= 9) {
            lblh.setText("0" + h + ":");
        } else {
            lblh.setText("" + h + ":");
        }
        if (m <= 9) {
            lblm.setText("0" + m + ":");
        } else {
            lblm.setText("" + m + ":");
        }
        if (s <= 9) {
            lbls.setText("0" + s + ":");
        } else {
            lbls.setText("" + s + ":");
        }
        if (ms <= 9) {
            lblms.setText("0" + ms);
        } else {
            lblms.setText("" + ms);
        }
    } // Fin actualizar

    // Inicio ctrlIniciar
    private void ctrlIniciar() {
        btnPausar.setEnabled(true);
        btnParar.setEnabled(true);
        btnIniciar.setEnabled(false);
        btnIniciar.setText("Reanudar");

    } // Fin ctrlIniciar

    // Inicio ctrlPausar
    private void ctrlPuasar() {
        btnIniciar.setEnabled(true);
        btnPausar.setEnabled(false);

    }// Fin ctrlPausar

    // Iniciar ctrlParar
    private void ctrlDetener() {
        btnIniciar.setEnabled(true);
        btnIniciar.setText("Iniciar");
        btnPausar.setEnabled(false);
        btnParar.setEnabled(false);
    }// Fin ctrlParar

    // Inicio reset
    private void reset() {
        ms = s = m = h = 0;
        actualizar();
    } // </editor-fold> 


    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        timer.start();
        ctrlIniciar();
    }//GEN-LAST:event_btnIniciarActionPerformed

    private void btnPausarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPausarActionPerformed
        timer.stop();
        ctrlPuasar();
    }//GEN-LAST:event_btnPausarActionPerformed

    private void btnPararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPararActionPerformed
        if (timer.isRunning()) {
            timer.stop();
        }
        ctrlDetener();
        reset();
    }//GEN-LAST:event_btnPararActionPerformed

